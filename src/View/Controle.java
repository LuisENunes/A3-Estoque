    private void preencherTabela() {
        try {

            EntradasDAO dao = new EntradasDAO();
            List<Model.Entradas> lista = dao.listarEntradas();
            
            DefaultTableModel modelo = (DefaultTableModel) tabentrada.getModel();
            modelo.setRowCount(0);

            for (Model.Entradas s : lista) {
                Object[] linha = {
                    s.getId(),
                    s.getProduto(),
                    s.getDataEntrada(),
                    s.getQuantidade(),
                    s.getFornecedor()
                };
                modelo.addRow(linha);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher tabela: " + e);
        }
    }

