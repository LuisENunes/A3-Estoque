    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int idEntrada = (int) tabentrada.getValueAt(tabentrada.getSelectedRow(), 0);

            EntradasDAO dao = new EntradasDAO();
            dao.excluirEntrada(idEntrada);
            preencherTabela();

            JOptionPane.showMessageDialog(null, "Entrada excluida com sucesso!");
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir" + erro);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

