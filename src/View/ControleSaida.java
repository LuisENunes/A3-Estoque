    private void preencherTabela() {
        try {

            SaidaDAO dao = new SaidaDAO();
            List<Model.Saidas> lista = dao.listarSaidas();
            
            DefaultTableModel modelo = (DefaultTableModel) tabsaida.getModel();
            modelo.setRowCount(0);

            for (Model.Saidas s : lista) {
                Object[] linha = {
                    s.getId(),
                    s.getProduto(),
                    s.getDataSaida(),
                    s.getQuantidade(),
                    s.getDestinatário()
                };
                modelo.addRow(linha);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher tabela: " + e);
        }
    }

