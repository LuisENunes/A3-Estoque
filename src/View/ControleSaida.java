    private void btdelsaidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdelsaidaActionPerformed
        try {
            int idSaida = (int) tabsaida.getValueAt(tabsaida.getSelectedRow(), 0);

            SaidaDAO dao = new SaidaDAO();
            dao.excluirSaida(idSaida);
            preencherTabela();

            JOptionPane.showMessageDialog(null, "Saida excluida com sucesso!");
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir" + erro);
        }
    }//GEN-LAST:event_btdelsaidaActionPerformed

