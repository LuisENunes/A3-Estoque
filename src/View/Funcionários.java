    private void preencherTabela() {
        try {
            this.idEdicao = "";

            FuncionarioDAO dao = new FuncionarioDAO();
            List<Model.Funcionario> lista = dao.listarFuncionarios();
            
            // Pega o modelo da tabela
            DefaultTableModel modelo = (DefaultTableModel) tablista.getModel();

            // Limpa antes de inserir novamente
            modelo.setRowCount(0);

            // Insere os dados
            for (Model.Funcionario f : lista) {
                Object[] linha = {
                    f.getId(),
                    f.getNome(),
                    f.getCargo(),
                };
                modelo.addRow(linha);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher tabela: " + e);
        }
    }

