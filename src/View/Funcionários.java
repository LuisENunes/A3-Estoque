    private void botaoCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadActionPerformed
        try {
            String nome  = txtnome.getText();
            String cargo = txtcargo.getText();
            String login = txtlogin.getText();
            String senha = txtsenha.getText();

            Model.Funcionario funcionario = new Model.Funcionario();
            funcionario.setNome (nome );
            funcionario.setCargo(cargo);
            funcionario.setLogin(login);
            funcionario.setSenha(senha);

            FuncionarioDAO dao = new FuncionarioDAO();

            if (this.idEdicao != null && !this.idEdicao.trim().isEmpty()) {
                dao.salvarEdicaoFuncionarios(Integer.parseInt(this.idEdicao), funcionario);
                JOptionPane.showMessageDialog(null, "Funcion치rio atualizado com sucesso!");
                this.idEdicao = "";
            } else {
                dao.cadastrarFuncionario(funcionario);
                JOptionPane.showMessageDialog(null, "Funcion치rio cadastrado com sucesso!");
            }

            preencherTabela();
            limpacampos();
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar funcionario" + erro);
        }
    }//GEN-LAST:event_botaoCadActionPerformed

