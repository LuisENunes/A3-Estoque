    private void botaoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoBuscarActionPerformed
        try {
            String pesquisa = txtbusca.getText();

            if (pesquisa.isEmpty()){
                this.preencherTabela();
            } else {
                FuncionarioDAO dao = new FuncionarioDAO();
                List<Model.Funcionario> lista = dao.pesquisarFuncionarios((String) pesquisa);

                DefaultTableModel modelo = (DefaultTableModel) tablista.getModel();
                
                modelo.setRowCount(0);

                for (Model.Funcionario f : lista) {
                    Object[] linha = {
                        f.getId(),
                        f.getNome(),
                        f.getCargo(),
                    };
                    modelo.addRow(linha);
                }
            }
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao pesquisar Funcionario" + erro);
        }
    }//GEN-LAST:event_botaoBuscarActionPerformed

    private void botaoExActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExActionPerformed
        try {
            String id = (String) tablista.getValueAt(tablista.getSelectedRow(), 0);

            FuncionarioDAO dao = new FuncionarioDAO();
            dao.excluirProduto(Integer.parseInt(id));
            preencherTabela();

            JOptionPane.showMessageDialog(null, "Funcionario excluido com sucesso!");
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir" + erro);
        }
    }//GEN-LAST:event_botaoExActionPerformed

    private void botaoAttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAttActionPerformed
        try {
            String id    = (String) tablista.getValueAt(tablista.getSelectedRow(), 0);
            String nome  = (String) tablista.getValueAt(tablista.getSelectedRow(), 1);
            String cargo = (String) tablista.getValueAt(tablista.getSelectedRow(), 2);

            FuncionarioDAO dao = new FuncionarioDAO();
            List<Model.Funcionario> lista = dao.pesquisarAtualizar(id);

            if (!lista.isEmpty()) {
                Model.Funcionario funcionario = lista.get(0);
                this.idEdicao = funcionario.getId();
                txtnome.setText(funcionario.getNome());
                txtcargo.setText(funcionario.getCargo());
                txtlogin.setText(funcionario.getLogin());
                txtsenha.setText(funcionario.getSenha());
            }


        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir" + erro);
        }
    }//GEN-LAST:event_botaoAttActionPerformed

