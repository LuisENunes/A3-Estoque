    private void preencherTabela() {
        try {

            CadastroDAO dao = new CadastroDAO();
            List<Model.CadastroProdutos> lista = dao.listarProdutos();
            
            // Pega o modelo da tabela
            DefaultTableModel modelo = (DefaultTableModel) tabelaconsulta.getModel();

            // Limpa antes de inserir novamente
            modelo.setRowCount(0);

            // Insere os dados
            for (Model.CadastroProdutos p : lista) {
                Object[] linha = {
                    p.getId(),
                    p.getProduto(),
                    (p.getQuantidade() > 0 ? "Sim" : "Não"),
                    p.getQuantidade(),
                    p.getCategoria()
                };
                modelo.addRow(linha);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher tabela: " + e);
        }
    }


