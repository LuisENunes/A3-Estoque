    private void botaoconsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoconsultaActionPerformed
        try {
            String pesquisa = txtprodcon.getText();

            if (pesquisa.isEmpty()){
                this.preencherTabela();
            } else {
                CadastroDAO dao = new CadastroDAO();
                List<Model.CadastroProdutos> lista = dao.pesquisarNomeProdutos((String) pesquisa);

                DefaultTableModel modelo = (DefaultTableModel) tabelaconsulta.getModel();
                
                modelo.setRowCount(0);

                for (Model.CadastroProdutos p : lista) {
                    Object[] linha = {
                        p.getId(),
                        p.getProduto(),
                        (p.getQuantidade() > 0 ? "Sim" : "Não"),
                        p.getQuantidade(),
                        p.getCategoria()
                    };
                    modelo.addRow(linha);
                }
            }
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao pesquisar Funcionario" + erro);
        }
    }//GEN-LAST:event_botaoconsultaActionPerformed

