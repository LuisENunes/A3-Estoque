    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {
        try {  
            int idProduto = (int) tabcad.getValueAt(tabcad.getSelectedRow(), 0);

            CadastroDAO dao = new CadastroDAO();
            dao.excluirProduto(idProduto);
            preencherTabela();

            JOptionPane.showMessageDialog(null, "Produto excluido com sucesso!");
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir" + erro);
        }
    }


    private void botaopesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaopesquisarActionPerformed
        try {
            String pesquisa = txtpesquisar.getText();
            if (pesquisa.trim().isEmpty()){
                this.preencherTabela();
            } else {
                // if (!(Integer.parseInt(pesquisa) instanceof Integer) ||  Integer.parseInt(pesquisa) <= 0) {
                //     JOptionPane.showMessageDialog(null, "Valor digitado invalido!");
                //     return;
                // }

                CadastroDAO dao = new CadastroDAO();
                List<Model.CadastroProdutos> lista = dao.pesquisarProduto(Integer.parseInt(pesquisa));

                if (lista != null) {
                    DefaultTableModel modelo = (DefaultTableModel) tabcad.getModel();
                    modelo.setRowCount(0);

                    for (Model.CadastroProdutos p : lista) {
                        Object[] linha = {
                            p.getId(),
                            p.getProduto(),
                            p.getQuantidade(),
                            p.getValor(),
                            p.getFornecedor(),
                            p.getCategoria()
                        };
                        modelo.addRow(linha);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Produto não encontrado");
                }
            }
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao pesquisar produto" + erro);
        }
    }//GEN-LAST:event_botaopesquisarActionPerformed

